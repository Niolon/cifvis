/**
 * @typedef {object} FractionalLimits
 * @property {number} minX - Minimum fractional X coordinate.
 * @property {number} maxX - Maximum fractional X coordinate.
 * @property {number} minY - Minimum fractional Y coordinate.
 * @property {number} maxY - Maximum fractional Y coordinate.
 * @property {number} minZ - Minimum fractional Z coordinate.
 * @property {number} maxZ - Maximum fractional Z coordinate.
 */

/**
 * Creates a set of symmetry elements that will fill the complete cell when executed subsequently
 * on the fragment grown from previously grown sets.
 * @param {CellSymmetry} symmetry - Cell symmetry for which the minimal set should be determined
 * @param {string[]} preexistingSymOps - Symmetry elements already generated by growing of fragment
 * @returns {Set<string>} - Set containing the ids of the unique set
 */
export function minimalGrowthSet(symmetry, preexistingSymOps) {
    const keepSet = new Set([symmetry.identitySymOpId]);

    const preexistingSet = new Set();

    // Add all symmetry operations that can be generated from preexisting ones
    for (const existId of preexistingSymOps) {
        const combinedId = symmetry.combineSymmetryCodes(existId + '_555', symmetry.identitySymOpId + '_555');
        preexistingSet.add(combinedId.split('_')[0]);
    }

    // Find minimal set needed to generate all symmetry operations
    for (const [id] of symmetry.operationIds) {
        if (keepSet.has(id) || preexistingSet.has(id)) {
            continue;
        }

        keepSet.add(id);
    }
    return keepSet;
}

/**
 * Calculates the minimum and maximum fractional coordinates for all atoms.
 * @param {Atom[]} atoms - The atoms to analyze
 * @returns {FractionalLimits} An object containing the min/max fractional coordinates
 */
export function getFragmentLimits(atoms) {
    if (atoms.length === 0) {
        return { minX: 0, maxX: 1, minY: 0, maxY: 1, minZ: 0, maxZ: 1 };
    }

    let minX = Infinity, maxX = -Infinity;
    let minY = Infinity, maxY = -Infinity;
    let minZ = Infinity, maxZ = -Infinity;

    for (const atom of atoms) {
        const { x, y, z } = atom.position;
        minX = Math.min(minX, x);
        maxX = Math.max(maxX, x);
        minY = Math.min(minY, y);
        maxY = Math.max(maxY, y);
        minZ = Math.min(minZ, z);
        maxZ = Math.max(maxZ, z);
    }

    return { minX, maxX, minY, maxY, minZ, maxZ };
}

/**
 * Calculates the center of a fragment defined by its atoms.
 * @param {Atom[]} atoms - The atoms defining the fragment
 * @returns {math.Matrix} The center of the fragment as a 3D vector
 */
export function getFragmentCentre(atoms) {
    const limits = getFragmentLimits(atoms);
    return math.matrix([
        (limits.minX + limits.maxX) / 2,
        (limits.minY + limits.maxY) / 2,
        (limits.minZ + limits.maxZ) / 2,
    ]);
}

/**
 * Calculates the symmetry-transformed centre.
 * @param {math.Matrix} centre - Original centre
 * @param {SymmetryOperation} symOp - Symmetry operation
 * @returns {math.Matrix} The symmetry converted center of the fragment as a 3D vector
 */
export function getSymmetryCentre(centre, symOp) {

    const symmCentre = math.add(
        math.multiply(symOp.rotMatrix, centre),
        symOp.transVector,
    );

    return symmCentre;
}

/**
 * Extracts already applied symmetries from atom labels in a group.
 * @param {object} group - Group of atoms
 * @param {CrystalStructure} structure - Crystal structure containing symmetry information
 * @param {Map<string, string>} specialPositionMap - Map of special positions (duplicates
 * @returns {string[]} Array of unique symmetry operation IDs for the group
 */
export function getGrownSymmetriesofGroup(group, structure, specialPositionMap) {
    const identitySymOp = structure.symmetry.identitySymOpId;
    const groupSymmetries = new Set();

    for (const atom of group.atoms) {
        if (atom.appliedSymmetry) {
            groupSymmetries.add(atom.appliedSymmetry.id);
        } else {
            groupSymmetries.add(identitySymOp);
        }
    }

    const atomIdsInGroup = new Set(group.atoms.map(atom => atom.uniqueId));

    for (const [originalId, mapTargetId] of specialPositionMap) {
        let symOpId = identitySymOp;
        // Parse ID directly since it's now guaranteed to be label|symmetry
        // Format: Label|SymID_Trans
        if (originalId.includes('|')) {
            const symKey = originalId.split('|')[1];
            symOpId = symKey.split('_')[0];
        }

        if (atomIdsInGroup.has(mapTargetId)) {
            groupSymmetries.add(symOpId);
        }
    }

    return Array.from(groupSymmetries);
}

/**
 * Checks if an atom position is within the unit cell.
 *
 * The upper bound is the packing cutoff: with the default 1.0 an atom sitting on
 * the far face (fractional 1.0) is treated as the same periodic position as 0.0 and
 * wrapped in, which keeps the cell contents (Z) correct. Raising it slightly (e.g.
 * 1.001) instead keeps those upper-border atoms, giving a "closed" packing diagram
 * at the cost of duplicating face/edge/corner atoms.
 * @param {Atom} atom - Atom to check
 * @param {number} [cutoff] - Upper fractional bound (packing cutoff)
 * @param {number} [tolerance] - Tolerance for boundary comparisons
 * @returns {boolean} True if atom is within unit cell
 */
function isWithinUnitCell(atom, cutoff = 1, tolerance = 1e-6) {
    const { x, y, z } = atom.position;
    // Atoms at position >= cutoff - tolerance are wrapped to the same periodic
    // position one cell lower.
    return x >= -tolerance && x < cutoff - tolerance &&
        y >= -tolerance && y < cutoff - tolerance &&
        z >= -tolerance && z < cutoff - tolerance;
}

/**
 * Creates a unique key for atom position to detect duplicates/special positions.
 * @param {Atom} atom - Atom to create key for
 * @param {number} [precision] - Decimal places for rounding
 * @returns {string} Position key
 */
function getAtomPositionKey(atom, precision = 3) {
    // Use toFixed for consistent string representation to avoid floating-point precision issues
    // (e.g., 0.14485 and 0.14484999999999992 should produce the same key)
    const x = atom.position.x.toFixed(precision);
    const y = atom.position.y.toFixed(precision);
    const z = atom.position.z.toFixed(precision);
    // Use the base chemical label from the atom, which is now just 'label'
    return `${atom.label}_x${x}_y${y}_z${z}`;
}

/**
 * Transforms a symmetry string to center it around a given symmetry centre.
 * @param {CellSymmetry} symmetry - Cell symmetry object
 * @param {string} symmString - Symmetry string to transform
 * @param {object} symmCentre - Centre coordinates to center the symmetry string around
 * @returns {string} Transformed symmetry string
 */
export function centreSymmetryString(symmetry, symmString, symmCentre) {
    const { symOp, transVector } = symmetry.parsePositionCode(symmString);

    const symOpResult = math.add(
        math.multiply(symOp.rotMatrix, math.matrix(symmCentre)),
        symOp.transVector,
    );

    const transformedCentre = math.add(symOpResult, transVector);

    const offsetX = Math.floor(transformedCentre.get([0]));
    const offsetY = Math.floor(transformedCentre.get([1]));
    const offsetZ = Math.floor(transformedCentre.get([2]));

    const originalSymmetry = symmString.split('_')[0];

    const originalTranslationString = symmString.split('_')[1] || '555';
    const translationParts = originalTranslationString.split('').map(part => {
        return parseInt(part, 10); // Default to 5 if part is not a number
    });

    const newTranslationString = (
        `${translationParts[0] - offsetX}`
        + `${translationParts[1] - offsetY}`
        + `${translationParts[2] - offsetZ}`
    );

    const offsetCentre = math.subtract(transformedCentre, math.matrix([offsetX, offsetY, offsetZ]));
    return { newCentre: offsetCentre, newString: `${originalSymmetry}_${newTranslationString}` };
}

